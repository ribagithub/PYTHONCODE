def add():
    a=20                   #Local variable declaration(variables which are defined inside the function)
    b=40                   #Local variable declaration                  
    print("add=",a+b)
add()                      

def add():
    a=25
    b=85
    c=76
    print("a=",a)
    print("b=",b)
    print("c=",c)
    print("Total of a+b+c=",a+b+c)
add()

def subtract():
    a,b,c=25,85,76
    print("a =",a,"b =",b,"c =",c)
    print("Total of a-b-c =",a-b-c)
subtract()
