#Bitwise operator
a=int(input("enter number 1")) #150
b=int(input("enter number 2")) #22
c=a&b                          #Bitwise AND operator
print("a&b=",c)
c=a|b                          #Bitwise OR operator
print("a|b=",c)
c=a<<b                         #Bitwise left shift
print("a<<b=",c)
c=a>>b                         #Bitwise right shift
print("a>>b=",c)