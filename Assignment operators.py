#Assignment operators
a=int(input("enter number 1"))  #76
b=int(input("enter number 2"))  #46
a+=b
print("a+=b: ",a)               #a=a+b
a-=b
print("a-=b: ",a)               #a=a-b
a*=b
print("a*=b: ",a)               #a=a*b
a/=b
print("a/=b: ",a)               #a=a/b
a%=b
print("a%=b: ",a)               #a=a%b